# Import the math library
import math

# Calculate the square root of 16
sqrt_16 = math.sqrt(16)
print("Square root of 16:", sqrt_16)

# Calculate the sine of 90 degrees (converted to radians)
sin_90 = math.sin(math.radians(90))
print("Sine of 90 degrees:", sin_90)

# Calculate the factorial of 5
factorial_5 = math.factorial(5)
print("Factorial of 5:", factorial_5)


# Calculate the power of 2 raised to 8
power_2_8 = math.pow(2, 8)
print("2 raised to the power of 8:", power_2_8)
